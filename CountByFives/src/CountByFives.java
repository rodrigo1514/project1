
public class CountByFives {

	public static void main(String[] args) {
		{
			
			{
			final int START = 5;
			final int STOP = 500;
			final int NUMBER_PER_LINE = 50;
			for(int i = START; i <= STOP; i += START)
			{
			System.out.print(i + " ");
			if(i % NUMBER_PER_LINE == 0)
			System.out.println();
			}
			
			

	}

}
}
}